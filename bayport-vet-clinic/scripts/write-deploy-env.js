#!/usr/bin/env node
/**
 * Writes assets/deploy-env.js from process.env.BAYPORT_API_BASE (Netlify / Vercel build).
 * Usage: BAYPORT_API_BASE=https://xxx.koyeb.app/api node scripts/write-deploy-env.js
 */
const fs = require("fs");
const path = require("path");

const INVALID = [/<[^>]*>/, /your-azure-app/i, /your-app-xxxxx/i, /example\.com/i, /placeholder/i];

function normalizeApiBase(raw) {
  let base = String(raw || "").trim().replace(/\/+$/, "");
  if (!base) return "";
  if (/^https?:\/\/[^/]+$/i.test(base)) base += "/api";
  return base;
}

function isValidApiBase(url) {
  const base = normalizeApiBase(url);
  if (!base) return false;
  if (INVALID.some((re) => re.test(base))) return false;
  try {
    const u = new URL(base);
    if (u.protocol !== "http:" && u.protocol !== "https:") return false;
    const host = u.hostname || "";
    if (!host || host.includes("<") || host.includes(">")) return false;
    return true;
  } catch {
    return false;
  }
}

const raw = process.env.BAYPORT_API_BASE || "";
const base = isValidApiBase(raw) ? normalizeApiBase(raw) : "";

if (process.env.NETLIFY === "true" && !base) {
  console.error(
    "[write-deploy-env] ERROR: Set BAYPORT_API_BASE in Netlify (e.g. https://your-app.koyeb.app/api)",
  );
  process.exit(1);
}

if (raw.trim() && !base) {
  console.warn("[write-deploy-env] Ignored invalid BAYPORT_API_BASE:", raw.trim());
}

const outPath = path.join(__dirname, "..", "assets", "deploy-env.js");
const body = `/**
 * Generated by scripts/write-deploy-env.js — do not edit by hand.
 */
window.BAYPORT_API_BASE = ${JSON.stringify(base)};
window.USE_API = true;
`;
fs.writeFileSync(outPath, body, "utf8");
console.log("[write-deploy-env] wrote", outPath, base ? base : "(empty — local dev uses localhost API)");
